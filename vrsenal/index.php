								<header class="major">
									<h3>Why I chose Early Access for VRsenal</h3>
									</header>
									<p>VRsenal is still in its early stages of development. So I would love to show the community what I have done so far and hear what everyone has to say as far as mechanics go. Things are a bit rusty at the moment but they will shape up soon!</p>
										<h3>Approximately how long will this game be in Early Access?</h3>
										<p>Originally I planned for this game to go straight to the market. After realizing that the amount of content was a tad bit low, I decided to go to early access. With the Early Access / initial release slated for July 4th, 2017. I am planning for VRsenal to be out of Early Access by the summer of 2018.</p>
										<h3>How is the full version planned to differ from the Early Access version?</h3>
										<p>There will be more weapons, game modes and places to explore / shoot up. I am also open to suggestions as far as content and overall game design goes.</p>
										<h3>What is the current state of the Early Access version?</h3>
										<p>The current state of the Early Access version is pre alpha. The game certainly isn't finished but with the help of the community this game will be a fun experience for everyone!</p>
									<h3>Will the game be priced differently during and after Early Access?</h3>
								<p>I plan to gradually raise the price leading up to the final release. That will only happen when most of the major content that I have planned is in place.</p>
							<h3>How are you planning on involving the Community in your development process?</h3>
						<p>I will keep the community involved by closely monitoring the forums and reading every suggestion from our players. We will let our players partake in beta builds so they can let us know what they like and what they dislike. I will also have a VRsenal community discord where players can always stay in touch with me or my moderators.</p>
